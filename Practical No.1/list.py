#wap to perform list operation

list1=["apple","banana","mango"]
print(list1)
#append in list
list1.append("Grapes")
print(list1)
#insert in list
list1.insert(1,"pineapple")
print(list1)
#removing from the list
list1.remove("banana")
print(list1)
#deleting by index 
list1.pop(0)
print(list1)
#deleting end of list
list1.pop()
print(list1)
#Adding elements in the list
list1.append("apple")
print(list1)
list1.append("mango")
print(list1)
#slicing
print(list1[1:])
print(list1[0:1])
